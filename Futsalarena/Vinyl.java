    private void nampilindata (){
        try {
            cn = koneksidatabase.BukaKoneksi();
            st = cn.createStatement();
            rs = st.executeQuery("SELECT * FROM vinyl");
            
            DefaultTableModel model = new DefaultTableModel ();
            model.addColumn("No.");
            model.addColumn("Tanggal Pemesanan");
            model.addColumn("Nama Team");
            model.addColumn("Waktu Booking");
            model.addColumn("Pembayaran");
            
            int no = 1;
            model.getDataVector ().removeAllElements();
            model.fireTableDataChanged();
            model.setRowCount(0);
            
            while (rs.next()) {
                Object[] data = {
                    no++,
                    rs.getString("tanggal_pemesanan"),
                    rs.getString("nama_team"),
                    rs.getString("waktu_booking"),
                    rs.getString("pembayaran")
            };
            model.addRow(data);
            tblData.setModel (model);
            }
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(null, "Data tidak dapat di tampilkan","Error", JOptionPane.INFORMATION_MESSAGE);
            
        }
    }

