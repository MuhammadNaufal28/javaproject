    private void btnbookingActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnbookingActionPerformed
        // TODO add your handling code here:
        try {
            st = cn.createStatement();
            if (fieldnama.getText().equals("") || 
                    fieldwaktu.getText().equals("") ||
                    fieldpayment.getText().equals("") ||
                    fieldstatus.getText().equals("")){
              JOptionPane.showMessageDialog(null, "Anda belum mengisi semua form","Error", JOptionPane.HEIGHT);
            return;
            }
            
            //cek nama team sudah ada atau belom
            
            if(btnbooking.getText()== "Nama_Team"){
                String cekdata = "SELECT * FROM vinyl WHERE Nama_Team = '" + fieldwaktu.getText() + "'";
                rs = st.executeQuery(cekdata);
                if (rs.next()) {
                    JOptionPane.showMessageDialog(null, "ups... nama team sudah ada");
                    hilangkan ();
                   
                }
            }else{
                    String masuksql = "INSERT INTO vinyl VALUES ('" + fieldnama.getText() +
                            "','" + fieldwaktu.getText() +
                            "','" + fieldpayment.getText() +
                            "','" + fieldstatus.getText() + "')";
                    
                    st.executeUpdate(masuksql);
                    JOptionPane.showMessageDialog(null, "Berhasil di booking");
                    nampilindata();
                    hilangkan();
                   
                    
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_btnbookingActionPerformed

