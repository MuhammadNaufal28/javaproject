    private void btnhapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnhapusActionPerformed
        // TODO add your handling code here:
        if (fieldnama.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Silahkan Pilih data yang ingin anda hapus");
        }else {
            int jawab = JOptionPane.showConfirmDialog(null,"Data ini akan dihapus, Lanjutkan?", "konfirmasi", JOptionPane.OK_CANCEL_OPTION);
            if (jawab == 0){
                try {
                    st = cn.createStatement();
                    String sql = "DELETE FROM vinyl WHERE tanggal_pemesanan = '" + fieldnama.getText() + "'";
                    st.executeUpdate(sql);
                    JOptionPane.showMessageDialog(null, "Data Berhasil dihapus");
                    nampilindata();
                    hilangkan();
                    
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, e);
                }
            }
        }
                
    }//GEN-LAST:event_btnhapusActionPerformed

